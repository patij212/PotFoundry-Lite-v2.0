/**
 * Export mesh to OBJ format
 *
 * @param mesh - Mesh data to export
 * @param options - Export options
 * @returns Blob containing the OBJ file
 */
export async function exportToOBJ(
  mesh: MeshData,
  options: ExportOBJOptions = {}
): Promise<Blob> {
  const { onProgress } = options;

  onProgress?.(0, 'Generating OBJ data...');

  // Use streaming for large meshes
  const STREAMING_THRESHOLD = 500_000;
  const useStreaming =
    mesh.vertexCount > STREAMING_THRESHOLD ||
    mesh.triangleCount > STREAMING_THRESHOLD;

  let blob: Blob;

  if (useStreaming) {
    onProgress?.(0.3, 'Streaming large mesh...');
    blob = generateStreamingOBJ(mesh, options);
  } else {
    const content = generateOBJString(mesh, options);
    blob = new Blob([content], { type: 'text/plain' });
  }

  onProgress?.(1.0, 'OBJ export complete');

  console.log(
    `[ExportOBJ] Created OBJ: ${(blob.size / 1024).toFixed(1)}KB ` +
      `(${mesh.triangleCount.toLocaleString()} triangles)`
  );

  return blob;
}

/**
 * Download mesh as OBJ file
 */
export async function downloadOBJ(
  mesh: MeshData,
  filename: string = 'pot.obj',
  options: ExportOBJOptions = {}
): Promise<void> {
  const blob = await exportToOBJ(mesh, {
    ...options,
    name: options.name ?? filename.replace(/\.obj$/i, ''),
  });

  // Create download
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}

/**
 * Estimate OBJ file size
 *
 * OBJ files are text-based, so they're larger than binary STL but smaller
 * than ASCII STL due to vertex sharing.
 */
export function estimateOBJSize(
  mesh: { vertexCount: number; triangleCount: number },
  includeNormals: boolean = true
): number {
  // Vertex lines: "v x.xxxxx y.yyyyy z.zzzzz\n" ≈ 35 bytes
  const vertexData = mesh.vertexCount * 35;

  // Normal lines: "vn nx ny nz\n" ≈ 35 bytes per face
  const normalData = includeNormals ? mesh.triangleCount * 35 : 0;

  // Face lines: "f v1//n1 v2//n2 v3//n3\n" ≈ 30-50 bytes depending on indices
  const faceData = mesh.triangleCount * 40;

  // Header overhead
  const headerOverhead = 200;

  return headerOverhead + vertexData + normalData + faceData;
}
